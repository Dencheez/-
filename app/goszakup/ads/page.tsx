function AnnouncementCard({ item }: { item: typeof announcements[0] }) {
    const [isOpen, setIsOpen] = useState(false)
    const downloads = (item as any).downloads as DownloadLink[] | undefined

    return (
        <div className="bg-white rounded-2xl border border-slate-100 shadow-sm overflow-hidden transition-all hover:shadow-md">
            <button
                onClick={() => setIsOpen(!isOpen)}
                className="w-full p-6 md:p-8 flex flex-col md:flex-row gap-4 md:gap-8 items-start md:items-center text-left"
            >
                <div className="w-14 h-14 bg-[#00B5C4]/10 rounded-2xl flex items-center justify-center text-[#00B5C4] shrink-0">
                    <Megaphone className="w-7 h-7" />
                </div>

                <div className="flex-1 min-w-0">
                    <div className="flex flex-wrap items-center gap-3 mb-2">
                        <span className="text-[10px] font-black text-[#00B5C4] uppercase tracking-widest">{item.type}</span>
                        <span className="w-1 h-1 bg-slate-200 rounded-full" />
                        <div className="flex items-center gap-1.5 text-slate-400">
                            <Calendar className="w-3.5 h-3.5" />
                            <span className="text-[10px] font-black uppercase tracking-widest">{item.date}</span>
                        </div>
                    </div>
                    <h3 className="text-sm font-black text-slate-800 uppercase tracking-tight leading-snug">{item.title}</h3>
                </div>

                <div className="shrink-0 w-10 h-10 rounded-xl bg-slate-50 flex items-center justify-center text-slate-400 transition-colors">
                    {isOpen ? <ChevronUp className="w-5 h-5" /> : <ChevronDown className="w-5 h-5" />}
                </div>
            </button>

            {isOpen && (
                <div className="px-6 md:px-8 pb-8 border-t border-slate-50 overflow-x-auto">
                    <div className="pt-6 text-[13px] md:text-[14px] leading-relaxed text-slate-600 whitespace-pre-wrap min-w-[300px]">
                        {item.content}
                    </div>

                    {downloads && downloads.length > 0 && (
                        <div className="mt-8 flex flex-wrap gap-3">
                            {downloads.map((download, index) => (
                                <a key={index} href={download.href} download className="inline-block">
                                    <button className="flex items-center gap-2 px-6 py-3 bg-slate-900 text-white rounded-xl font-black uppercase text-[10px] tracking-widest hover:bg-[#00B5C4] transition-all">
                                        <Download className="w-4 h-4" />
                                        <span>{download.label}</span>
                                    </button>
                                </a>
                            ))}
                        </div>
                    )}
                </div>
            )}
        </div>
    )
}

